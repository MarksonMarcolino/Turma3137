    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
    String nome,cpf,rg,cargo,setor,especialidade,logradouro = null;
        double salario;
        float renda = 0;
        if(!(jTFCpf.getText().equals("") ||
           jTFNome.getText().equals("") ||
           jTFSalario.getText().equals("") ||
           jTFCargo.getText().equals("") ||
           jTFSetor.getText().equals("") ||
           jTFEspecialidade.getText().equals("") ||
           jTFRg.getText().equals(""))){
           nome = jTFNome.getText();
           cpf = jTFCpf.getText();
           rg = jTFRg.getText();
           cargo = jTFCargo.getText();
           setor = jTFSetor.getText();
           especialidade = jTFEspecialidade.getText();
           try{
           salario = Double.parseDouble (jTFSalario.getText());
           BancoVendedor.getInstance().vendedores.add(new Vendedor (setor, cargo, salario,nome, rg,cpf,logradouro,renda,especialidade));
           jTFNome.setText("");
           jTFCpf.setText("");
           jTFRg.setText("");
           jTFSalario.setText("");
           jTFCargo.setText("");
           jTFSetor.setText("");
           jTFEspecialidade.setText("");
           }catch(NumberFormatException nfe){
               JOptionPane.showMessageDialog(null,"Valor da renda inv√°lido!!\n"+nfe);
           }
        }else{
            JOptionPane.showMessageDialog(null,"Campo em branco!!");
                }
    }//GEN-LAST:event_jBSalvarActionPerformed

