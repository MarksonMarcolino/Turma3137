    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome, CPF, RG, setor, cargo, especialidade;
        Funcionario supervisor;
        float salario, comissao;
        

        if(!(txtNome.getText().equals("")||txtCPF.getText().equals("")||txtRG.getText().equals("")||txtSetor.getText().equals("")
            ||txtCargo.getText().equals("")||txtSalario.getText().equals("")||txtEspecialidade.getText().equals(""))){

        nome = txtNome.getText(); CPF = txtCPF.getText(); RG = txtRG.getText(); setor = txtSetor.getText();
        cargo = txtCargo.getText(); especialidade = txtEspecialidade.getText();
        

        try{
            comissao = Float.parseFloat(txtComissao.getText());
            salario = Float.parseFloat(txtSalario.getText());
            int id = Integer.parseInt(txtID.getText());
            Vendedor buscado = VendedorSingleton.getInstance().vendedores.get(id);
            supervisor = VendedorSingleton.getInstance().vendedores.get(Integer.parseInt(txtIDsupervisor.getText()));
            
            buscado.setNome(nome);
            buscado.setCpf(CPF);
            buscado.setRg(RG);
            buscado.setSetor(setor);
            buscado.setCargo(cargo);
            buscado.setEspecialidade(especialidade);
            buscado.setSalario(salario);
            buscado.setComissao(Float.parseFloat(txtComissao.getText()));
            buscado.setSupervisor(supervisor);
            buscado.setSalariof(salario+comissao);
            
            txtNome.setText("");
            txtCPF.setText("");
            txtRG.setText("");
            txtCargo.setText("");
            txtSetor.setText("");
            txtSalario.setText("");
            txtEspecialidade.setText("");
            txtComissao.setText("");
            txtSupervisor.setText("");
            txtIDsupervisor.setText("");
            txtID.setText("");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erro: "+ e);
        }

        }else{
            JOptionPane.showMessageDialog(null,"Todos os campos devem ser preenchidos!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

