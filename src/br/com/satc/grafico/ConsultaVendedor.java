    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
    VendedorTableModel vtm = null;
    
    
     if (jTPesquisa.getText().equals("")){
     JOptionPane.showMessageDialog(this,"Campo em branco.");
     }
     else if (jRBCodigo.isSelected()){       
     try{
     vtm = new VendedorTableModel(BancoVendedor.getInstance().pesquisaCodigo(
     Integer.parseInt(jTPesquisa.getText()), BancoVendedor.getInstance().vendedores));
     }catch(IndexOutOfBoundsException ioobe){
         JOptionPane.showMessageDialog(this, "Vendedor inexistente.");
     }
     }else{
     vtm = new VendedorTableModel(BancoVendedor.getInstance().pesquisaNome(
     jTPesquisa.getText(), BancoVendedor.getInstance().vendedores));
     }
      jTabela.setModel(vtm);
      
    
    }//GEN-LAST:event_jBPesquisarActionPerformed


