    private void jBPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPesquisarActionPerformed
        ModeloTabela vtm;
        if (jRBNome.isSelected()){
            vtm = new ModeloTabela(Singleton.getInstance().pesquisaNome(jTFCampoPesquisa.getText(), Singleton.getInstance().vendedores));
        }else{
            vtm = new ModeloTabela(Singleton.getInstance().pesquisaCodigo(Integer.parseInt(jTFCampoPesquisa.getText()), Singleton.getInstance().vendedores));
        }
        jTabela.setModel(vtm);
    }//GEN-LAST:event_jBPesquisarActionPerformed

