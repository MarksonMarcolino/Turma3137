    private void jBEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEditarActionPerformed
        String nome,rg,cpf,setor,cargo,especialidade;
        float salario,comissao;

        if (!(jTNome.getText().equals("") || jTRG.getText().equals("")
            || jTCPF.getText().equals("") || jTSetor.getText().equals("")
            || jTCargo.getText().equals("") || jTComissao.getText().equals("")
            || jTEspecialidade.getText().equals("") || jTSalario.getText().equals(""))){

        nome = jTNome.getText();
        rg = jTRG.getText();
        cpf = jTCPF.getText();
        setor = jTSetor.getText();
        cargo = jTCargo.getText();
        especialidade= jTEspecialidade.getText();
        try{
            salario = Float.parseFloat(jTSalario.getText());
            comissao = Float.parseFloat(jTComissao.getText());
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setNome(nome);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setRg(rg);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setCpf(cpf);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setSetor(setor);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setCargo(cargo);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setEspecialidade(especialidade);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setSalario(salario);
            BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).setComissao(comissao);
            JOptionPane.showMessageDialog(this, "Êxito na Edição.");
            jTCPF.setText("");
            jTCargo.setText("");
            jTEspecialidade.setText("");
            jTNome.setText("");
            jTRG.setText("");
            jTSalario.setText("");
            jTSetor.setText("");
            jTComissao.setText("");
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(this,"Salário ou Comissão inválidos : \n"+nfe);
        }
        }else{
            JOptionPane.showMessageDialog(this,"Campos em Branco.");

        }
    }//GEN-LAST:event_jBEditarActionPerformed

