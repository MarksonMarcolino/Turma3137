    private void jBProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBProcurarActionPerformed
     if (jTID.getText().equals("")){
     JOptionPane.showMessageDialog(this,"Campo em branco.");
     }else{
    try{
    jTNome.setText(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getNome());
    jTRG.setText(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getRg());
    jTCPF.setText(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getCpf());
    jTSetor.setText(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getSetor());
    jTCargo.setText(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getCargo());
    jTSalario.setText(Float.toString(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getSalario()));
    jTEspecialidade.setText(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getEspecialidade());
    jTComissao.setText(Float.toString(BancoVendedor.getInstance().vendedores.get(Integer.parseInt(jTID.getText())).getComissao()));
    }catch(IndexOutOfBoundsException ioobe){
         JOptionPane.showMessageDialog(this, "Vendedor inexistente.");
     }
     }
    }//GEN-LAST:event_jBProcurarActionPerformed


